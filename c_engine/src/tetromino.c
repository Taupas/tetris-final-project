int piece_type_color(int type) {
    if (type < 0 || type >= PIECE_TYPES) {
        return 0;
    }
    return type + 1;
}

void piece_get_cells(int type, int rotation, int x, int y, int out_rows[4], int out_cols[4]) {
    int i, count = 0;
    if (type < 0 || type >= PIECE_TYPES) {
        for (i = 0; i < 4; i++) {
            out_rows[i] = -1;
            out_cols[i] = -1;
        }
        return;
    }
    rotation %= 4;
    for (i = 0; i < 16; i++) {
        int sr = i / 4;
        int sc = i % 4;
        if (SHAPES[type][rotation][sr][sc]) {
            out_rows[count] = y + sr - 1;
            out_cols[count] = x + sc - 1;
            count++;
        }
    }
    while (count < 4) {
        out_rows[count] = -1;
        out_cols[count] = -1;
        count++;
    }
}
